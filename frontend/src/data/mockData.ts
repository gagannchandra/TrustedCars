function getCarImages(make: string, model: string, carId: string): Car['images'] {
  const key = `${make}-${model}`;
  const imgs = CAR_IMAGES[key] || CAR_IMAGES['default'];
  return imgs.map((url, i) => ({
    id: `img-${carId}-${i}`,
    car_id: carId,
    url,
    caption: ['Front View', 'Side View', 'Interior'][i] || 'View',
    is_primary: i === 0,
    sort_order: i,
  }));
}

function makeInspection(carId: string, overall: number) {
  const base = Math.round(overall / 10);
  return {
    id: `insp-${carId}`,
    car_id: carId,
    inspector_name: 'TrustedCars Certified Inspector',
    inspected_at: '2024-01-15T10:00:00Z',
    engine_score: Math.min(10, base + (Math.random() > 0.5 ? 1 : 0)),
    transmission_score: Math.min(10, base),
    suspension_score: Math.min(10, base - 1),
    brakes_score: Math.min(10, base + (Math.random() > 0.5 ? 1 : 0)),
    electricals_score: Math.min(10, base),
    ac_score: Math.min(10, base),
    interior_score: Math.min(10, base - (Math.random() > 0.5 ? 1 : 0)),
    exterior_score: Math.min(10, base - (Math.random() > 0.5 ? 1 : 0)),
    tyre_score: Math.min(10, base - 1),
    overall_score: overall,
    findings: {
      engine: { status: overall >= 85 ? 'good' : 'fair', notes: overall >= 85 ? 'No leaks, smooth idle, good compression' : 'Minor wear, runs well' },
      tyres: { front_left: 70, front_right: 75, rear_left: 80, rear_right: 80 },
      paint: { touched_up: overall < 85 ? ['front_bumper'] : [], original: ['hood', 'doors', 'roof', 'fenders'] },
      accident_history: overall < 75,
      flood_damage: false,
      odometer_genuine: true,
    },
  };
}

function makeServiceRecords(carId: string, count: number) {
  const services = ['Oil Change', 'Major Service', 'Brake Service', 'AC Service', 'Tyre Rotation'];
  const centers = ['Maruti Service', 'Hyundai Care', 'Honda Service', 'Authorized Workshop'];
  return Array.from({ length: count }, (_, i) => ({
    id: `svc-${carId}-${i}`,
    car_id: carId,
    service_date: `${2023 - i}-${String(Math.ceil(Math.random() * 12)).padStart(2, '0')}-15`,
    odometer_at_service: 10000 * (i + 1),
    service_center: centers[i % centers.length],
    service_type: services[i % services.length],
    notes: 'All fluids topped up, filters replaced',
  }));
}

